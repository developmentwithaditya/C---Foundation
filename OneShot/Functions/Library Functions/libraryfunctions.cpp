#include <iostream>
#include <cmath>
using namespace std;

int main()
{
/*
 Library functions are built-in function in c++.

 Example: If you have calculate the minimum & maximum of numbers we can use the built-in function in c++ like: 
*/

cout << "Maximum of a, b is: " << max(2,8) << endl; // We only need to call that function and pass the arguments.
cout << "Minimum of a, b is: " << min(2,8) << endl;
cout << "Square root of a is: " << sqrt(8) << endl;
cout << "Cube root of a is: " << cbrt(1000) << endl;
cout << "Power of a is: " << pow(10, 2) << endl;

    return 0;
}